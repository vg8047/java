class SwitchDemo{

	public static void main (String[] args){

		int num=3;

		switch(num){

			case 1:
				System.out.println("one");

			case 2:
			 
			        System.out.println("two");
			default :
			  
			          System.out.println("in default state");

		}
      System.out.println("after switch");
 }
} 
