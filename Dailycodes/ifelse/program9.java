class SwitchDemo{

	public static void main(String[] args){

		float num =2.4f;

		switch(num){

			case 1.1f:

				  System.out.println("1.1");
				  break;

			case 1.2f:
		                 
		                     System.out.println("1.2");
		                     break;
		         case 1.4f:
		          
	                            System.out.println("2.4");
	                            break;
	                   
	                     default :
	                                 System.out.println("in default state");
	                           
			     case 2.4f:
			                System.out.println("2.4");
		                        break;
		}
	}
}	
